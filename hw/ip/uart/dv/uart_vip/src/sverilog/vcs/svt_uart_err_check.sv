    */
   svt_err_check_stats svt_err_chk_sout_by_dte_before_dtr  ;
   
   /**
    * Check : This error message is triggered when DTR is de-asserted during
    *         downstream data transfer 
    */
   svt_err_check_stats svt_err_chk_dtr_deassert_during_data_d  ;
   
   /**
    * Check : This error message is triggered when DSR is de-asserted during
    *         downstream data transfer
    */
   svt_err_check_stats svt_err_chk_dsr_deassert_during_data_d  ;
   
   /**
    * Check : This error message is triggered when DTR is de-asserted during upstream data
