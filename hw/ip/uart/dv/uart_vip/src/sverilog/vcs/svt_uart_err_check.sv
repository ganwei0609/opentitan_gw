   /**
    * Check : This error message is triggered when CTS is asserted by DCE before RTS
    *         is asserted 
    */
   svt_err_check_stats svt_err_chk_cts_before_dsr  ; 
   
   /**
    * Check : This error message is triggered when SIN is asserted by DCE before DSR
