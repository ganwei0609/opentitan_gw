   /**
    * Check : This error message is triggered when DSR is de-asserted by DCE during
    *         RTS/CTS handshake 
    */
   svt_err_check_stats svt_err_chk_dsr_deassert_during_hndshk  ;
   
   /** Check : This warning message is triggered when RTS is de-asserted by DTE during
