   /**
    * Check : This error message is triggered when data on sout is observed when de is deasserted
    */
   svt_err_check_stats svt_err_rs485_no_derive_sout_if_de_off ; 
  
   /**
    * Check : This error message is triggered when data on sin is observed when re is deasserted
